package com.ashutosh.backend.enums;

/**
 * Defines the gearbox types available for vehicles in the fleet.
 * Helps users identify and select vehicles based on their driving preference.
 */
public enum VehicleTransmission {
    /**
     * Represents a gearbox that requires the driver to change gears manually.
     */
    MANUAL,

    /**
     * Represents a gearbox that shifts gears automatically during operation.
     */
    AUTOMATIC
}